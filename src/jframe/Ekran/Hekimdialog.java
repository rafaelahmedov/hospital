private void action(String actionname){
boolean New , Edit , SCC , CAN, done , del;
   switch(actionname){
       case "New": { New=false;
                     Edit=false;
                     SCC=true;
                     done=true;
                     del=false;
                     CAN=true;
                   break;}
      case "Edit":{  New=false;
                     Edit=false;
                     SCC=true;
                     done=true;
                     del=false;
                     CAN=true;
                   break;}
      case "Table":{ New=true;
                     Edit=true;
                     SCC=false;
                     done=false;
                     del=true;
                     CAN=true;
                     break;}
    
      default:       New=true;
                     Edit=false;
                     SCC=false;
                     done=false;
                     del=false;
                     CAN=true;
                     clear();
                    
    }
    buttonproperties(New, Edit, SCC, del, CAN);
    textfieldproperties(done);
}
