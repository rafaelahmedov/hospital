private void fillthetable(List<Hekim>list){
DefaultTableModel tmodel = new DefaultTableModel(){
    @Override
    public boolean isCellEditable(int i, int i1) {
        return false;}};
tmodel.addColumn("Ad");
tmodel.addColumn("Soyad");
tmodel.addColumn("Telefon");
tmodel.addColumn("Vezife");
tmodel.addColumn("Sobe");
jTable1.setModel(tmodel);
    for (Hekim hekim : list) {
       tmodel.insertRow(jTable1.getRowCount(), 
               new Object[] { hekim.getAd(),
                              hekim.getSoyad(),
                              hekim.getTelefon(),
                              hekim.getVezife(),
                              conn.idgoreekrancixarsobe(hekim.getIdsobe()).get(0).getAd()
       });
    }
}   
