private void fillthetable(List<Pasiyent>list){
    DefaultTableModel tmodel = new DefaultTableModel(){
        @Override
        public boolean isCellEditable(int i, int i1) {
            return false;}};
    tmodel.addColumn("Ad");
    tmodel.addColumn("Soyad");
    tmodel.addColumn("Pinkod");
    tmodel.addColumn("Telefon");
    tmodel.addColumn("Adress");
    jTable1.setModel(tmodel);
    for (Pasiyent pas : list){ 
     tmodel.insertRow(jTable1.getRowCount(), new Object[]{pas.getAd(),pas.getSoyad(),pas.getPinkod(),pas.getTelefon(),pas.getAdres()});
    }
   }    
  
