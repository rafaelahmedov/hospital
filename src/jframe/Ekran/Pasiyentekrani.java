private void action(String actionname){
    boolean New , done , SCC , Del, CAN , Edit ; 
    switch (actionname){
        case "New":{
            New = false;
            Edit=false;
            done= true; 
            SCC=true;
            Del=false;
            CAN=true;
           
               break;     
        }
        case "Table" :{
            New = true;
            Edit=true;
            done= false; 
            SCC=false;
            Del=true;
            CAN=true;
               break;    
        }
        case "Edit" :{
            New = false;
            Edit=false;
            done= true; 
            SCC=true;
            Del=false;
            CAN=true;
               break;    
        }
        default:
            New = true;
            Edit=false;
            done= false; 
            SCC=false;
            Del=false;
            CAN=true;
            
               break;  
    }
    textfealdproperties(done);
    buttonproperties(New, Edit, SCC,Del ,CAN );
}

