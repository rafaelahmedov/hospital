private void action(String actionname){
boolean New,Done,SCC,Edit,Del,Can;
switch(actionname){
    case "New":{     New=false;
                     Edit=false;
                     SCC=true;
                     Done=true;
                     Del=false;
                     Can=true;
                   break;}
    case"Edit":{     New=false;
                     Edit=false;
                     SCC=true;
                     Done=true;
                     Del=false;
                     Can=true;
                   break;}
    case "Table":{ New=true;
                     Edit=true;
                     SCC=false;
                     Done=false;
                     Del=true;
                     Can=true;
                     break;}
     default:       New=true;
                     Edit=false;
                     SCC=false;
                     Done=false;
                     Del=false;
                     Can=true;
                     clear();}
    buttonproperties(New, SCC, Edit, Del, Can);
        textfealdproperties(Done);


} 
private void fillthetable(List<sobe>list){
    DefaultTableModel tmodel = new DefaultTableModel(){
        @Override
        public boolean isCellEditable(int i, int i1) {
            return false;}};
    tmodel.addColumn("Ad");
    tmodel.addColumn("Melumat");
    jTable1.setModel(tmodel);
    for (sobe s : list) {
        tmodel.insertRow(jTable1.getRowCount(), new Object[]{s.getAd(),s.getMelumat()});
    }
}
