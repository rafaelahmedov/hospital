    private void jButtonsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsaveActionPerformed
      if(!jTextFieldad.getText().equals("")&&!jTextFieldmelumat.getText().equals("")){
        int id = 0;
        if(selectedsobe!=null){
        id=selectedsobe.getIdsobe();}
        connmysqlxestexana conn = new connmysqlxestexana();
        sobe s = new sobe(0, jTextFieldad.getText(), jTextFieldmelumat.getText());
        s.setIdsobe(id);
        conn.insertupdatesobe(s);
        listofsobe=conn.ekranacixarmasobe("");
          fillthetable(listofsobe);
          action("");
        selectedsobe=null;
        clear();
        }else{
          JOptionPane.showMessageDialog(null, "Bos saxlamaq olmaz!!");
      
      }
    }//GEN-LAST:event_jButtonsaveActionPerformed

